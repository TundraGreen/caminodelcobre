    <h3>Trail Segments and Motor Transport</h3>
      The Camino is for hikers who want to enjoy walking outdoors.  However, in practical terms, many hikers may want to spend some portion of a day hiking and the rest of the day at some other recreational activity.  Therefore, the Camino is designed to be experienced in discrete, and somewhat independent, segments (similar to Trails in other countries).  At convenient points along the Camino the hiker can leave the trail, walk ~50 meters to a junction with Highway 77, and return to Creel via some form of motor transportation.  Alternatively, the hiker may take a vehicle from Creel to an access point, and return to town walking on the Camino.<br />

      The most convenient vehicle transport is with one of the many transportation companies in Creel.  In many cases it is possible to arrange direct transport either to or from an access point for a reasonable cost.<br />

      Alternatively, some hikers may prefer to use buses.  Bus transportation is a very common throughout Mexico.  The Creel and entire Barrancas region has a well-developed system of bus transportation.  There are many buses that regularly travel along the main Creel-to-Divisadero highway.   Using buses for some portion of a day’s hiking is feasible.<br />

