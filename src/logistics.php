      <h3>Trail Markers</h3>
      The Camino is indicated by simple green and red trail markers roughly 5 X 10 centimeters (2 X 4 inches) painted primarily on trees or rocks.  The markers are generally horizontal bars.  In certain situations the markers are also found on telephone poles, fence posts, concrete walls, buildings, even the concrete ties of the railroad – anywhere they are easily visible and helpful to the hiker who doesn’t know the route.

      The markers are generally 30 - 40 meters apart.  In cases where the trail is difficult to follow, or very important, the markers are more frequent – sometimes as close as 5 meters or less.  On some paths that are easy to follow (for example, a long straight road with no intersecting paths), the markers are farther apart.  Their purpose is primarily to provide reassurance that the hiker is indeed on the correct path. <br />

      From one marker it should be possible to see the next marker along the trail.  If not immediately visible, hikers should walk forward following their previous direction (e.g., straight) while looking for the next marker on nearby trees or rocks.  It should be visible within 15 meters.<br />

      In addition to the principal markers showing the correct path, the Camino also shows a large red “X” on trails that are NOT correct.  These are typically found at intersections where there might be some uncertainty about which path to take.<br />

      Moreover, to show where there are turns or gentler “bends” in the path, the trail markers show the oncoming turn, either sharply or in a gradual “Y” curve.
